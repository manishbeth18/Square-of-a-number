import java.util.*;
public class Square {
    public static void main(String[] args){
        Scanner sc = new Scanner(System.in);
        System.out.println("Enter a number for the square of it.");
        int n = sc.nextInt();
        
        int d=n*n;
        
        System.out.println("The square of the given  number is "+d);
        
    }

}
